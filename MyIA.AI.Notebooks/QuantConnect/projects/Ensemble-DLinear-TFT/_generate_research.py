        "class DLinearModel(nn.Module):\n",
        "    def __init__(self, seq_len, input_dim, n_classes=2):\n",
        "        super().__init__()\n",
        "        self.seq_len = seq_len\n",
        "        self.linear = nn.Linear(seq_len * input_dim, n_classes)\n",
        "\n",
        "    def forward(self, x):\n",
        "        B, L, D = x.shape\n",
        "        return self.linear(x.reshape(B, -1))\n",
        "\n",
        "\n",
        "class TFTLiteModel(nn.Module):\n",
        "    def __init__(self, input_dim, d_model=32, n_heads=2, n_classes=2):\n",
        "        super().__init__()\n",
        "        self.input_proj = nn.Linear(input_dim, d_model)\n",
        "        self.lstm = nn.LSTM(d_model, d_model, num_layers=1, batch_first=True)\n",
        "        self.attn = nn.MultiheadAttention(d_model, n_heads, batch_first=True)\n",
        "        self.head = nn.Sequential(\n",
        "            nn.Linear(d_model, d_model // 2),\n",
        "            nn.ReLU(),\n",
        "            nn.Linear(d_model // 2, n_classes)\n",
        "        )\n",
        "\n",
        "    def forward(self, x):\n",
        "        h = self.input_proj(x)\n",
        "        h, _ = self.lstm(h)\n",
        "        h2, _ = self.attn(h, h, h)\n",
        "        h = h + h2\n",
        "        return self.head(h[:, -1, :])\n",
        "\n",
        "print('DLinear + TFT-Lite models defined')",
