        "def create_sequences(X, y, seq_len):\n",
        "    Xs, ys = [], []\n",
        "    for i in range(seq_len, len(X)):\n",
        "        Xs.append(X[i-seq_len:i])\n",
        "        ys.append(y[i])\n",
        "    return np.array(Xs), np.array(ys)\n",
        "\n",
        "\n",
        "def train_model(model, X_train, y_train, epochs, batch_size, device, lr=1e-3):\n",
        "    model = model.to(device)\n",
        "    optimizer = torch.optim.Adam(model.parameters(), lr=lr)\n",
        "    criterion = nn.CrossEntropyLoss()\n",
        "    X_t = torch.FloatTensor(X_train).to(device)\n",
        "    y_t = torch.LongTensor(y_train).to(device)\n",
        "    best_loss = float('inf')\n",
        "    patience = 5\n",
        "    wait = 0\n",
        "    for epoch in range(epochs):\n",
        "        model.train()\n",
        "        indices = torch.randperm(len(X_t))\n",
        "        total_loss = 0\n",
        "        n_batches = 0\n",
        "        for i in range(0, len(X_t), batch_size):\n",
        "            batch_idx = indices[i:i+batch_size]\n",
        "            batch_X = X_t[batch_idx]\n",
        "            batch_y = y_t[batch_idx]\n",
        "            logits = model(batch_X)\n",
        "            loss = criterion(logits, batch_y)\n",
        "            optimizer.zero_grad()\n",
        "            loss.backward()\n",
        "            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)\n",
        "            optimizer.step()\n",
        "            total_loss += loss.item()\n",
        "            n_batches += 1\n",
        "        avg_loss = total_loss / max(n_batches, 1)\n",
        "        if avg_loss < best_loss - 1e-4:\n",
        "            best_loss = avg_loss\n",
        "            wait = 0\n",
        "        else:\n",
        "            wait += 1\n",
        "            if wait >= patience:\n",
        "                break\n",
        "    return model\n",
        "\n",
        "\n",
        "def predict_probs(model, X_test, device):\n",
        "    model.eval()\n",
        "    with torch.no_grad():\n",
        "        X_t = torch.FloatTensor(X_test).to(device)\n",
        "        logits = model(X_t)\n",
        "        probs = F.softmax(logits, dim=-1)[:, 1].cpu().numpy()\n",
        "    return probs\n",
        "\n",
        "\n",
        "def simulate_strategy(pred_probs, actual_returns, tcost_bps=10):\n",
        "    tcost = tcost_bps / 10000\n",
        "    position = 0\n",
        "    daily_rets = []\n",
        "    for i in range(len(pred_probs)):\n",
        "        new_pos = 1 if pred_probs[i] > 0.5 else 0\n",
        "        cost = abs(new_pos - position) * tcost\n",
        "        ret = new_pos * actual_returns[i] - cost\n",
        "        daily_rets.append(ret)\n",
        "        position = new_pos\n",
        "    daily_rets = np.array(daily_rets)\n",
        "    sharpe = np.mean(daily_rets) / (np.std(daily_rets) + 1e-10) * np.sqrt(252)\n",
        "    cum_ret = np.prod(1 + daily_rets) - 1\n",
        "    return {'sharpe': sharpe, 'cum_return': cum_ret}\n",
        "\n",
        "print('Utilities defined')",
