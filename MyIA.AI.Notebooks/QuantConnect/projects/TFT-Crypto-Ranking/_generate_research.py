        "        super().__init__()\n",
        "        self.lstm = nn.LSTM(input_size, hidden_size, num_layers, batch_first=True)\n",
        "        self.fc = nn.Linear(hidden_size, 1)\n",
        "    def forward(self, x):\n",
        "        out, _ = self.lstm(x)\n",
        "        return self.fc(out[:, -1, :])\n",
        "\n",
        "\n",
        "# --- TFT Components ---\n",
        "\n",
        "class GatedResidualNetwork(nn.Module):\n",
        "    \"\"\"GRN with skip connection (Lim et al., 2021).\"\"\"\n",
        "    def __init__(self, input_dim, hidden_dim, output_dim=None, dropout=0.1):\n",
        "        super().__init__()\n",
        "        output_dim = output_dim or input_dim\n",
        "        self.fc1 = nn.Linear(input_dim, hidden_dim)\n",
        "        self.fc2 = nn.Linear(hidden_dim, output_dim)\n",
        "        self.gate_fc = nn.Linear(hidden_dim, output_dim)\n",
        "        self.layer_norm = nn.LayerNorm(output_dim)\n",
        "        self.dropout = nn.Dropout(dropout)\n",
        "        self.skip = nn.Linear(input_dim, output_dim) if input_dim != output_dim else nn.Identity()\n",
        "\n",
        "    def forward(self, x):\n",
        "        residual = self.skip(x)\n",
        "        hidden = F.elu(self.fc1(x))\n",
        "        hidden = self.dropout(hidden)\n",
        "        gate = torch.sigmoid(self.gate_fc(hidden))\n",
        "        gated = gate * self.fc2(hidden)\n",
        "        return self.layer_norm(residual + gated)\n",
        "\n",
        "\n",
        "class VariableSelectionNetwork(nn.Module):\n",
        "    \"\"\"VSN: learns feature importance (Lim et al., 2021).\"\"\"\n",
        "    def __init__(self, n_vars, hidden_dim, dropout=0.1):\n",
        "        super().__init__()\n",
        "        self.grn = GatedResidualNetwork(n_vars, hidden_dim, n_vars, dropout)\n",
        "\n",
        "    def forward(self, x):\n",
        "        weights = F.softmax(self.grn(x), dim=-1)\n",
        "        return x * weights, weights\n",
        "\n",
        "\n",
        "class TFTClassifier(nn.Module):\n",
        "    \"\"\"TFT adapted for binary ranking classification.\"\"\"\n",
        "    def __init__(self, n_vars, seq_len, d_model=32, n_heads=2,\n",
        "                 lstm_layers=1, dropout=0.1):\n",
        "        super().__init__()\n",
        "        self.input_proj = nn.Linear(n_vars, d_model)\n",
        "        self.vsn = VariableSelectionNetwork(n_vars, d_model, dropout)\n",
        "        self.lstm = nn.LSTM(d_model, d_model, lstm_layers, batch_first=True,\n",
        "                           dropout=dropout if lstm_layers > 1 else 0.0)\n",
        "        self.attention = nn.MultiheadAttention(d_model, n_heads, dropout=dropout,\n",
        "                                              batch_first=True)\n",
        "        self.pos_encoding = nn.Parameter(torch.randn(1, seq_len, d_model) * 0.02)\n",
        "        self.post_attn_grn = GatedResidualNetwork(d_model, d_model * 2, d_model, dropout)\n",
        "        self.decoder_grn = GatedResidualNetwork(d_model, d_model * 2, d_model, dropout)\n",
        "        self.output_proj = nn.Linear(d_model, 1)\n",
        "        self.final_norm = nn.LayerNorm(d_model)\n",
        "\n",
        "    def forward(self, x):\n",
        "        B, T, N = x.shape\n",
        "        selected, attn_weights = self.vsn(x)\n",
        "        encoded = self.input_proj(selected)\n",
        "        lstm_out, _ = self.lstm(encoded)\n",
        "        lstm_out = lstm_out + self.pos_encoding[:, :T, :]\n",
        "        attn_out, _ = self.attention(lstm_out, lstm_out, lstm_out)\n",
        "        attn_out = self.post_attn_grn(attn_out + lstm_out)\n",
        "        last_hidden = attn_out[:, -1, :]\n",
        "        decoded = self.decoder_grn(last_hidden)\n",
        "        decoded = self.final_norm(decoded)\n",
        "        return self.output_proj(decoded)\n",
