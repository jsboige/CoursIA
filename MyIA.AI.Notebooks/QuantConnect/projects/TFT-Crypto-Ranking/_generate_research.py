        "        super().__init__()\n",
        "        self.decomposition = SeriesDecomposition(kernel_size=25)\n",
        "        self.linear_seasonal = nn.Linear(seq_len, 1)\n",
        "        self.linear_trend = nn.Linear(seq_len, 1)\n",
        "    def forward(self, x):\n",
        "        seasonal, trend = self.decomposition(x)\n",
        "        seasonal_agg = seasonal.mean(dim=2)\n",
        "        trend_agg = trend.mean(dim=2)\n",
        "        return self.linear_seasonal(seasonal_agg) + self.linear_trend(trend_agg)\n",
        "\n",
