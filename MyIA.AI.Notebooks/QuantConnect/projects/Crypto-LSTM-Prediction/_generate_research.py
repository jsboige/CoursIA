        "class SeriesDecomposition(nn.Module):\n",
        "    \"\"\"Series decomposition block (DLinear, AAAI 2023).\"\"\"\n",
        "    def __init__(self, kernel_size=25):\n",
        "        super().__init__()\n",
        "        self.moving_avg = nn.AvgPool1d(kernel_size=kernel_size, stride=1,\n",
        "                                        padding=kernel_size // 2)\n",
        "\n",
        "    def forward(self, x):\n",
        "        trend = self.moving_avg(x.transpose(1, 2)).transpose(1, 2)\n",
        "        trend = trend[:, :x.shape[1], :]\n",
        "        seasonal = x - trend\n",
        "        return seasonal, trend\n",
        "\n",
        "\n",
        "class DLinear(nn.Module):\n",
        "    \"\"\"DLinear: decomposition + linear (AAAI 2023). Version shared-channel.\"\"\"\n",
        "    def __init__(self, seq_len=60, pred_len=1, enc_in=26):\n",
        "        super().__init__()\n",
        "        self.decomposition = SeriesDecomposition(kernel_size=25)\n",
        "        self.linear_seasonal = nn.Linear(seq_len, pred_len)\n",
        "        self.linear_trend = nn.Linear(seq_len, pred_len)\n",
        "\n",
        "    def forward(self, x):\n",
