        "        # Mean across channels then linear\n",
        "        seasonal_agg = seasonal.mean(dim=2)   # (batch, seq_len)\n",
        "        trend_agg = trend.mean(dim=2)\n",
        "        out = self.linear_seasonal(seasonal_agg) + self.linear_trend(trend_agg)\n",
        "        return out  # (batch, 1)\n",
